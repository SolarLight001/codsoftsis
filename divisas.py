#CONVERSOR DE DIVISAS
Yuan = 0.36
Yen = 7.40
Dólar = 0.049
Euro = 0.04
Libra = 0.039

pesos = input("Cuantos pesos quieres convertir")
print("Tus %d equivalen a:" , pesos)
pesos = int(pesos)

print((pesos * Yuan) , "%d Yuanes" ,(pesos * Yuan))
print((pesos * Yen) , "%d Yenes" ,(pesos * Yen))
print((pesos * Dólar) , "%d dolares" ,(pesos * Dólar))
print((pesos * Euro) , "%d Euros" ,(pesos * Euro))
print((pesos * Libra) , "%d Libra" ,(pesos * Libra))

